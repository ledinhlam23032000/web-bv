function toArticleSlug(title: string) {
  return title
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/đ/g, "d")
    .replace(/Đ/g, "d")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

export function getArticleSlug(article: { title: string }) {
  return toArticleSlug(article.title);
}

export function getArticleBySlug(slug: string) {
  return articleCatalog.find((article) => getArticleSlug(article) === slug);
}
